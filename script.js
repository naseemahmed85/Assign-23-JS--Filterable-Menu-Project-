const searchBox = document.getElementById('searchBox');
const searchForm = document.getElementById('searchForm');
const foodItemsDiv = document.getElementById('foodItems');

searchForm.addEventListener('submit', function (event) {
  event.preventDefault(); // form reload na kare

    const value = searchBox.value.toLowerCase();

  // agar kuch likha nahi gaya to sab items wapas dikha do
    if (value === '') {
        menu('All');
        return;
    }

  // filter karo matching title ke hisaab se
    const filtered = resturanMenu.filter(function (item) {
        return item.title.toLowerCase().startsWith(value);
    });

  // agar koi match nahi mila
    if (filtered.length === 0) {
        foodItemsDiv.innerHTML = `<p style="text-align:center; font-Size: 25px; display: flex; color:#c59d5f;">No matching items found!</p>`;
        return;
    }

  // matched items display karo
    const resultHTML = filtered.map(function (menu) {
    return `
        <div class="foodItem">
        <div class="imgDiv">
            <img src="${menu.imgUrl}" alt="${menu.title}">
        </div>
        <div class="titlDescription">
            <div class="titlePrice">
            <h2>${menu.title}</h2>
            <p>${menu.price}</p>
        </div>
            <p>${menu.description}</p>
        </div>
        </div>
    `;
    });

    foodItemsDiv.innerHTML = resultHTML.join('');
});




